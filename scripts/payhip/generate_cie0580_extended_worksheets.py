def parse_listing(listing_path: Path) -> tuple[str, str, list[str]]:
    text = listing_path.read_text(encoding="utf-8")
    lines = text.splitlines()

    kahoot_title = "CIE 0580 Extended Track"
    kahoot_url = DEFAULT_KAHOOT

    in_kahoot = False
    for i, line in enumerate(lines):
        stripped = line.strip()
        if stripped.startswith("# Kahoot"):
            in_kahoot = True
            continue
        if stripped == "---":
            in_kahoot = False
        if in_kahoot and stripped == "## Title:":
            for nxt in lines[i + 1 :]:
                if nxt.strip():
                    kahoot_title = nxt.strip()
                    break
        if in_kahoot and stripped == "## Link:":
            for nxt in lines[i + 1 :]:
                candidate = nxt.strip()
                if not candidate:
                    continue
                if candidate.startswith("##") or candidate.startswith("---"):
                    break
                if candidate.startswith("http"):
                    kahoot_url = candidate
                break

    if "insert your kahoot" in kahoot_url.lower() or not kahoot_url.startswith("http"):
        kahoot_url = DEFAULT_KAHOOT

    tags: list[str] = []
    in_tags = False
    for line in lines:
        stripped = line.strip()
        if stripped == "## Tags:":
            in_tags = True
            continue
        if in_tags:
            if stripped.startswith("#"):
                break
            if stripped:
                tags.append(stripped)

    return kahoot_title, kahoot_url, tags


def latex_bullet_line(items: list[str]) -> str:
    return " $\\bullet$\n  ".join(items)


def render_question_block(questions: list[str], resume: bool) -> str:
    opener = (
        r"\begin{enumerate}[resume, label=\qnum{\arabic*}, leftmargin=3.2em, itemsep=12pt]"
        if resume
        else r"\begin{enumerate}[label=\qnum{\arabic*}, leftmargin=3.2em, itemsep=12pt]"
    )
    rows = [opener]
    for q in questions:
        suffix = "" if re.search(r"[.?!:]$", q.strip()) else "."
        rows.append(f"  \\item {q}{suffix}\\MarkOne \\AnswerBox")
    rows.append(r"\end{enumerate}")
    return "\n".join(rows)


def render_answer_rows(start_number: int, answers: list[str]) -> str:
    rows = []
    for idx, ans in enumerate(answers, start=start_number):
        rows.append(f"{idx} & {ans} & \\tickbox & \\textcolor{{textmuted}}{{---}} \\\\")
    return "\n".join(rows)


