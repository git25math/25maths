def generate_topic(topic: dict) -> None:
    folder = BASE_DIR / topic["folder"]
    listing_path = folder / topic["listing"]
    worksheet_path = folder / f"{topic['code']}-Worksheet.tex"

    kahoot_title, kahoot_url, listing_tags = parse_listing(listing_path)
    if not listing_tags:
        listing_tags = [
            "IGCSE Math",
            "CIE 0580",
            "0580 Extended",
            topic["name"],
            "Math Worksheet",
            "Kahoot Quiz",
            "Cambridge IGCSE",
            "Math Revision",
        ]

    questions = topic["questions"]
    answers = topic["answers"]
    if len(questions) != 15 or len(answers) != 15:
        raise ValueError(f"{topic['code']} must contain exactly 15 questions and 15 answers")

    rendered = BASE_TEMPLATE
    replacements = {
        "__TOPIC_NAME__": topic["name"],
        "__TOPIC_CODE__": topic["slug"],
        "__KAHOOT_URL__": kahoot_url,
        "__KAHOOT_TITLE__": kahoot_title,
        "__FOCUS_LINE__": topic["focus_line"],
        "__INSTRUCTION_LINE__": topic["instruction"],
        "__SKILLS_LINE__": latex_bullet_line(topic["skills_line"]),
        "__SECTION_A_TITLE__": topic["section_a_title"],
        "__SECTION_A_SKILLS__": topic["section_a_skills"],
        "__SECTION_B_TITLE__": topic["section_b_title"],
        "__SECTION_B_SKILLS__": topic["section_b_skills"],
        "__SECTION_C_TITLE__": topic["section_c_title"],
        "__SECTION_C_SKILLS__": topic["section_c_skills"],
        "__SECTION_A_QUESTIONS__": render_question_block(questions[:5], resume=False),
        "__SECTION_B_QUESTIONS__": render_question_block(questions[5:10], resume=True),
        "__SECTION_C_QUESTIONS__": render_question_block(questions[10:], resume=True),
        "__TEACHER_TIP__": topic["teacher_tip"],
        "__SECTION_A_ANSWERS__": render_answer_rows(1, answers[:5]),
        "__SECTION_B_ANSWERS__": render_answer_rows(6, answers[5:10]),
        "__SECTION_C_ANSWERS__": render_answer_rows(11, answers[10:]),
        "__TOPIC_TAGS__": " $\\bullet$ ".join(listing_tags),
    }

    for token, value in replacements.items():
        rendered = rendered.replace(token, value)

    worksheet_path.write_text(rendered, encoding="utf-8")


